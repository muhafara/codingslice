<p>
Java programs and most of the programming languages have some keywords. To write a simple java program, we need to follow a combination of the keywords. Java is case sensitive; for example, public and Public both are different. In java, we called the public keyword an access modifier that allows us to define the scope or how other parts of your code or even some else code can access this code. We will discuss more these access modifier later. Every main java program should define inside a class, which means java is an object-oriented language and used to express a class with the name following the keyword with curly brackets, which is the body of the class.
</p>
<p>
Like other programming languages, the java program should have a main method as the main method is the initializing of the program. The main method is defined by the keyword reserved keyword main. 
</p>
<p>
static is a reserved keyword in java. Every java program should be static, which means java to find our method to run the code we will add. Variable, nested classes and method can be static. It means static can access these without creating objects.
</p>
<p>
