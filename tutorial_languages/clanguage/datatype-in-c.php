<h1 align="center">
<b>Datatypes and variables in C</b>
</h1>
<p>
Datatypes are a pre-defined format for data storage that a variable can perform a specific function. Datatypes specify the variable type, or it is a declaration of a variable.<br>
Datatypes can be defiend further as :<br>
1. Primitive data types<br>
2. Derived data types<br>
3. Enumerated data type<br>
4. Void datatypes
</p>
<h4><b><u>Primitive data types:</u></b></h4>
<p>In "c language" there are some pre-defined or fundamental types of data called "primitive data types".for example int, float, char.</p>
<h4><b><u>Derived data types:</u></b></h4>
<p>Derived data types are composed of basic data types. It shows basic data types in groups like an array, pointers, unions and structures.</p>
<h4><b><u>Enumerated data types:</u></b></h4>
<p>Enumerated data types also referred to as "enum". It holds constant values which can not be changed. Reserved keyword "enum" is used To define these types of data types.</p>
<h4><b><u>void:</u></b></h4>
<p>Void in "C language" is an empty data type that has no return value. Usually void is a function which has function declaration and definition but has no return typeâ€”for example, void function ().</p>
