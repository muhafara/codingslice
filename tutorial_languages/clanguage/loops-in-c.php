<h1 align="center">
<b>Loops in C</b>
</h1></div>
<p>In programming, language loops mean "repetition" it repeats the block of code until specific condition meet any criteria. A loop consists of two parts its body and its control statement, which depend on any condition. The control statement is executed until some condition is true, and it will break the repetition in loops when the condition is false. In "C" there are two types of loops which are.</p>
1- Entry Control Loops<br>
2- Exit Control Loop
<h4><b><u>Entry Control Loops</u></b></h4>
<p>In this loop, the condition is checked before entering or executing the body of the loop; it is also known as pre-entry checked loop. Example of entry control loops or pre-entry loops is "while Loops".</p>
<h4><b><u>Exit Control Loops</u></b></h4>
<p>In this loop, the condition is checked after entering or executing the body of the loop; it is also known as post-entry checked loops. Example of the exit-control loop is a do-while loop.</p>
<p>In "C" three types of loops are used which are:</p>
1- for loop<br>
2- while loop<br>
3- do..while loop
