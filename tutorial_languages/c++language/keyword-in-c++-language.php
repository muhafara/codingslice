<h1 align="center">
<b>Keywords in C++</b>
</h1>

<p>
''Keywords'' are the reserved or special meaning words in any programming language. Keywords cannot be used as an identifier, or it can not be used as a name of a variable name, function name, or any other thing in the program as it is reserved from use.
<p/>
<br>
<p>
Some examples of reserve keywords are break, case, char, default, do, double, else, enum, float, for, goto, if, inline, int, long, register, return, short, switch, void. 
</p>

